            WHEN SUBSTR(productname, 0, 5) LIKE '19%' 
                 OR SUBSTR(productname, 0, 5) LIKE '20%'
            THEN SUBSTR(productname, 0, 5)
            ELSE NULL
       END as year,
       CASE
